""" EXAMPLE 1
Below are 4 defined functions, multiply, divide, add, subtract. They are each called below and their results are printed out
"""
def multiply(x,y): 
  return x*y

def divide(x,y):
  return x/y

def add(x,y):
  return x + y

def subtract(x,y):
  return x - y

print(multiply(3,4))
print(divide(6,2))
print(add(20,4))
print(subtract(5,1))




